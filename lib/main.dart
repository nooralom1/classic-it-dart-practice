void main() {
  int marks = 60;

  if(marks>=80&&marks<=100){
    print("result is A+");
  }
  else  if(marks>=70&&marks<=79){
    print("result is A");
  }
  else  if(marks>=60&&marks<=69){
    print("result is A-");
  }
  else  if(marks>=50&&marks<=59){
    print("result is B+");
  }
  else  if(marks>=40&&marks<=49){
    print("result is B");
  }
  else  if(marks>=33&&marks<=39){
    print("result is C");
  }
  else{
    print("F");
  }
}
